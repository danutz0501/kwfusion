		<label>Why use kW Fusion? Is it better than "&lt; x-brand >" framework?</label>
		<div class="toggle-content">
			<p> There are many great PHP frameworks available. Each has it's own pro's and con's, it's own way of doing things, it's own philosophy and goals. Choosing a framework is often a personal choice -- whatever feels comfortable TO YOU is what you will inevitably fall back on and continue to use. As such, it can be difficult to objectively say one framework is better than another, since it is usually a personal preference. </p>
			<p> With that said, kW Fusion has several goals in mind. We think that a framework should have good performance. We also believe that it should support modern technologies and coding standards, rather than concern itself with legacy support...it is hard to move forward if you are stuck in the past, especially in the fast paced tech industry. We believe that a framework should have a clean, simple syntax that is as simple as possible to use and program in. Finally, we believe that a framework should have great documentation, plenty of useful features, the ability to create any type of website needed, and handle most of the background stuff for you. </p>
		</div>
